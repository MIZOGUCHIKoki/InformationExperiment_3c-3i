clear;
img = imread("kut.jpg");
gimg = 0.3*img(:,:,1) + 0.59*img(:,:,2) + 0.11*img(:,:,3);
threshold1 = 64;
threshold2 = 128;
threshold3 = 192;
threshold1_r = gimg > threshold1; 
threshold1_r = threshold1_r.*255;
threshold2_r = gimg > threshold2;
threshold2_r = threshold2_r.*255;
threshold3_r = gimg > threshold3;
threshold3_r = threshold3_r.*255;
figure;
imshow(threshold1_r);
imwrite(threshold3_r,"../Figures/05_041.png");
figure;
imshow(threshold2_r);
imwrite(threshold3_r,"../Figures/05_042.png");
figure;
imshow(threshold3_r);
imwrite(threshold3_r,"../Figures/05_043.png");